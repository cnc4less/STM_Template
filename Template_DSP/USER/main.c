#include "stm32f4xx.h"
#include "delay.h"
#include "led.h"
/*以下头文件的包含是为测试DSP库是否可用*/
#include "arm_math.h"
/*以下头文件的包含是为测试USOS是否支持*/
#include "cmsis_os.h"

/***********************************************************************************************************************************
--------------------------------------------------------------------------------------------------------------------------------------------------------------
Template_DSP&UCOS
--------------------------------------------------------------------------------------------------------------------------------------------------------------
|   该测试模板是为使用STM32F4ZGTx单片机的FPU（浮点运算单元）建立，同时支持UCOS-ii的开发
--------------------------------------------------------------------------------------------------------------------------------------------------------------
|   模板以跑马灯实验为基础，没有使用位带操作
--------------------------------------------------------------------------------------------------------------------------------------------------------------
|   version     3.0.1
|   date          2017.03.11
--------------------------------------------------------------------------------------------------------------------------------------------------------------
***********************************************************************************************************************************/
int main(void)
{
    delay_init(168);
    LED_Init();
    
    while (1) {
        delay_ms (300);
        ON (GPIOF,GPIO_Pin_9);
        OFF (GPIOF,GPIO_Pin_10);
        delay_ms (300);
        ON (GPIOF,GPIO_Pin_10);
        OFF (GPIOF,GPIO_Pin_9);
    }
}
